# Populated by load_image_cache(); maps img stem -> "data:image/png;base64,..."
IMG_DATA_URIS = {}


def load_image_cache():
    """Base64-encode every PNG in Manual/modules once; return dict[stem] -> data URI."""
    cache = {}
    if not IMG_DIR.is_dir():
        return cache
    for p in sorted(IMG_DIR.glob("*.png")):
        with p.open("rb") as f:
            b64 = base64.b64encode(f.read()).decode("ascii")
        cache[p.stem] = f"data:image/png;base64,{b64}"
    return cache


def load_yamls():
    """Return dict[slug] -> parsed YAML doc."""
    docs = {}
    for p in sorted(YAML_DIR.glob("*.yaml")):
        with p.open("r", encoding="utf-8") as f:
            doc = yaml.safe_load(f)
        if not doc:
            continue
        docs[doc["slug"]] = doc
    return docs


def escape_cpp(s):
    """Escape a Python string for a C++ double-quoted literal."""
    if s is None:
        return ""
    out = []
    for c in s:
        if c == "\\":
            out.append("\\\\")
        elif c == "\"":
            out.append("\\\"")
        elif c == "\n":
            out.append("\\n")
        elif c == "\r":
            out.append("\\r")
        elif c == "\t":
            out.append("\\t")
        else:
            out.append(c)
    return "".join(out)


