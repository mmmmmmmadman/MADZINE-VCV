def slug_to_img_stem(doc):
    """Best-effort mapping from slug -> lower-snake filename used in modules/ assets."""
    # If YAML had an image hint, prefer it; else fall back to snake_case.
    slug = doc["slug"]
    # Known special cases from migrate_to_yaml.py SLUG_TO_FILE:
    overrides = {
        "SwingLFO": "swing_lfo",
        "EuclideanRhythm": "euclidean_rhythm",
        "ADGenerator": "ad_generator",
        "PPaTTTerning": "ppattterning",
        "TWNCLight": "twnc_light",
        "TWNC2": "twnc_2",
        "QQ": "q_q",
        "DECAPyramid": "decapyramid",
        "EllenRipley": "ellen_ripley",
        "MADDYPlus": "maddy_",
        "EnvVCA6": "env_vca_6",
        "WeiiiDocumenta": "weiii_documenta",
        "UniversalRhythm": "universal_rhythm",
        "UniRhythm": "uni_rhythm",
        "SongMode": "songmode",
        "ALEXANDERPLATZ": "alexanderplatz",
        "SHINJUKU": "shinjuku",
        "theKICK": "thekick",
    }
    if slug in overrides:
        return overrides[slug]
    return slug.lower()


