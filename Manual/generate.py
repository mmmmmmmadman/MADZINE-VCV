def html_escape(s):
    if s is None:
        return ""
    return (
        s.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
    )


def lang_span(value_map, tag="p"):
    """Render three spans (en/zh/ja), one visible per active language."""
    parts = []
    for lang in ("en", "zh", "ja"):
        val = (value_map or {}).get(lang, "") or ""
        parts.append(
            f'<{tag} data-lang-group="{lang}">{html_escape(val)}</{tag}>'
        )
    return "".join(parts)


def lang_list(list_map):
    parts = []
    for lang in ("en", "zh", "ja"):
        items = (list_map or {}).get(lang) or []
        lis = "".join(f"<li>{html_escape(x)}</li>" for x in items)
        parts.append(
            f'<ul data-lang-group="{lang}">{lis}</ul>'
        )
    return "".join(parts)


def lang_html_raw(html_map):
    """manual_html_raw: insert raw HTML (already trusted, came from our own files)."""
    parts = []
    for lang in ("en", "zh", "ja"):
        raw = (html_map or {}).get(lang, "") or ""
        parts.append(f'<div data-lang-group="{lang}">{raw}</div>')
    return "".join(parts)


def render_card(doc, img_dir):
    """Small card for grid view."""
    slug = doc["slug"]
    name = html_escape(doc.get("name", slug))
    hp = doc.get("hp")
    hp_txt = f"{hp} HP" if hp is not None else ""
    stem = slug_to_img_stem(doc)
    img_src = IMG_DATA_URIS.get(stem) or f"{img_dir}/{stem}.png"
    desc_html = lang_span(doc.get("description"))
    feat_map = doc.get("features") or {}
    # Show first 3 features in card.
    feat_short = {
        lang: (feat_map.get(lang) or [])[:3] for lang in ("en", "zh", "ja")
    }
    return f"""
    <article class="card" data-slug="{slug}">
      <div class="card-top">
        <div class="card-img"><img src="{img_src}" alt="{html_escape(doc.get('alt') or name)}"></div>
        <div class="card-info">
          <h3>{name}</h3>
          <span class="hp">{hp_txt}</span>
          {desc_html}
        </div>
      </div>
      <div class="features">
        <h4 data-lang-group="en">Features</h4>
        <h4 data-lang-group="zh">功能</h4>
        <h4 data-lang-group="ja">機能</h4>
        {lang_list(feat_short)}
      </div>
    </article>
    """


